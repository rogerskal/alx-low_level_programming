#include "3-calc.h"

/**
 * op_add - calculate the sum of the two integers
 * @a: first integer
 * @b: second integer
 * return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate the sum of the two integers
 * @a: first integer
 * @b: second integer
 * return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculate the sum of the two integers
 * @a: first integer
 * @b: second integer
 * return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculate the sum of the two integers
 * @a: first integer
 * @b: second integer
 * return: division of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate the sum of the two integers
 * @a: first integer
 * @b: second integer
 * return: remainder of the division of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
